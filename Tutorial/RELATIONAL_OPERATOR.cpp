#include<iostream>
using namespace std ;

int main ()
{
    int a , b ;
    a=12;
    b=15;
    cout<<"IS EQUALS TO :"<<(a==b);
    cout<<"\nNOT EQUALS TO :"<<(a!=b);
    cout<<"\nGREATER THAN : "<<(a>b);
    cout<<"\nLESS THAN : "<<(a<b);
    cout<<"\nGREATER THAN OR EQUAL TO :"<<(a>=b);
    cout<<"\nLESS THAN OR EQUAL TO :"<<(a<=b);
    
    return 0;

}